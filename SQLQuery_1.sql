WITH T1 AS (
SELECT YEAR(ORDER_DATE) AS YEAR,
        MONTH(ORDER_DATE) AS MONTH,
        CEILING(MONTH(ORDER_DATE) / 6.0) AS Half_Year,
        SUM(REVENUE) - SUM(COGS) - SUM(DISCOUNT_AMOUNT) AS Total_Profit
    FROM fact_sales
    GROUP BY YEAR(ORDER_DATE), MONTH(ORDER_DATE)
    )
, T2 AS (
    SELECT YEAR, [MONTH], Half_Year, Total_Profit,
            Total_Profit *100 / SUM(Total_Profit) OVER(PARTITION BY [YEAR]) AS PERCENTAGE,
            SUM(Total_Profit) OVER(PARTITION BY [YEAR], Half_Year) AS Total_Profit_By_HalfYear
        FROM T1
    )
    SELECT *,
            CASE WHEN LAG(Total_Profit_By_HalfYear) OVER(ORDER BY [YEAR], [MONTH]) IS NULL THEN 0
            ELSE (Total_Profit_By_HalfYear - LAG(Total_Profit_By_HalfYear) OVER(ORDER BY [YEAR],[MONTH])) *100 / LAG(Total_Profit_By_HalfYear) OVER(ORDER BY [YEAR],[MONTH])
            END AS YoY_HalfMonth
        FROM T2
        ORDER BY [YEAR], [MONTH]


